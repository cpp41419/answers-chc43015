export function getAllQuestions(): (FAQQuestion & { slug: string })[] {
  return questions.map(q => ({
    ...q,
    slug: slugify(q.question)
  }));
}

export function getQuestionsByCategory(categoryName: string): (FAQQuestion & { slug: string })[] {
  return getAllQuestions().filter(q => q.category === categoryName);
}

export function getQuestionBySlug(categoryName: string, slug: string): (FAQQuestion & { slug: string }) | undefined {
  return getAllQuestions().find(q => q.category === categoryName && q.slug === slug);
}
